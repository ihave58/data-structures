const getMaxProduct = (nums: Array<number>): number => {
    let maxProduct = nums[0];
    let leftProduct = 1;
    let rightProduct = 1;

    for (let index = 0; index < nums.length; index++) {
        if (leftProduct === 0) {
            leftProduct = 1;
        }

        if (rightProduct === 0) {
            rightProduct = 1;
        }

        leftProduct = leftProduct * nums[index];
        rightProduct = rightProduct * nums[nums.length - index - 1];

        maxProduct = Math.max(maxProduct, Math.max(leftProduct, rightProduct));
    }

    return maxProduct;
};

